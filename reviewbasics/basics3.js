function countFavSounds (param){
    let contador = {};
    for (const user of param) {
        for (const sounds in user.favoritesSounds) {
            //console.log(sounds);
            if (sounds in contador){
                contador[sounds]++;
            } else {
                contador[sounds] = 1;
            }
        }  
    }
    return contador;
}
